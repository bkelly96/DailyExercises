package com.revature.day7;

public class HandsOnCalculator {

    public int add(int num1, int num2){
        int sum;

        sum = num1 +num2;

        return sum;
    }

    public int sub(int num1, int num2){
        int sum;

        sum = num1 - num2;

        return sum;
    }

    public double div(int num1, int num2){
        double sum;

        sum = num1/num2;

        return sum;
    }

    public int mult(int num1, int num2){
        int sum;
        sum = num1 * num2;
        return sum;
    }

}
